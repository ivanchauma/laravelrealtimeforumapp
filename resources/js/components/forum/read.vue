<template>
<show-question
:data = question
v-if="question"
>

</show-question>
</template>

<script>
import ShowQuestion from "./ShowQuestion.vue"

export default
{
  components: { ShowQuestion },
    data(){
        return {
            question:null
        }
    },
    created(){
        //console.log(this.$route.params.slug);
        axios.get(`/api/question/${this.$route.params.slug}`)
        .then(res => this.question = res.data.data)
    }
}
</script>

